#include <bits/stdc++.h>
using namespace std;
int main()
{
    int a = 100;
    int b = 200;
  
    cout << "**-- Comparison operators --**" << '\n';
    cout << "The value of a == b is " << (a == b) << '\n'; // equal -> 0
    cout << "The value of a != b is " << (a != b) << '\n'; // not equal -> 1
    cout << "The value of a >= b is " << (a >= b) << '\n'; // greater than or equal to -> 0
    cout << "The value of a > b is " << (a > b) << '\n'; // greater than -> 0
    cout << "The value of a <= b is " << (a <= b) << '\n'; // less than or equal to -> 1
    cout << "The value of a < b is " << (a < b) << '\n'; // less than -> 1

    return 0;
}