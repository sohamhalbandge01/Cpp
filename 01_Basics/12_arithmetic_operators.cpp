#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a = 12, b = 7;
    cout << "**-- Arithmetic operators --**" << '\n';
    cout << "The value of a + b is " << a + b << '\n';
    cout << "The value of a - b is " << a - b << '\n';
    cout << "The value of a * b is " << a * b << '\n';
    cout << "The value of a / b is " << a / b << '\n';
    cout << "The value of a % b is " << a % b << '\n';
    return 0;
}