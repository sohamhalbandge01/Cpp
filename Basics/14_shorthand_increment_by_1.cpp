#include <bits/stdc++.h>
using namespace std;

int main()
{
    int a = 12;
    cout << "The value of a++ is " << a++ << '\n'; // 12
    cout << "The value of a-- is " << a-- << '\n'; // 13
    cout << "The value of ++a is " << ++a << '\n'; // 13
    cout << "The value of --a is " << --a << '\n'; // 12

    return 0;
}